# Create an empty dictionary and add key-value pairs to it
wedding = {}
wedding["Cake"] = 1
wedding["Car"] = 1
wedding["Ribbons"] = 100
print(wedding)

# Iterate through all key-value pairs in the dictionary
wedding = {"Cakle": 1, "Car": 1, "Ribbons": 100}
for key in wedding.keys():
    print(key)

# Iterate through all values in the dictionary
for value in wedding.values():
    print(value)